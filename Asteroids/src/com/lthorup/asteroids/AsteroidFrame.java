package com.lthorup.asteroids;

import java.awt.BorderLayout;
import java.awt.EventQueue;

import javax.swing.JFrame;
import javax.swing.JPanel;
import javax.swing.border.EmptyBorder;

public class AsteroidFrame extends JFrame {

	private JPanel contentPane;
    private KeyBoard keys;

	/**
	 * Launch the application.
	 */
	public static void main(String[] args) {
		EventQueue.invokeLater(new Runnable() {
			public void run() {
				try {
					AsteroidFrame frame = new AsteroidFrame();
					frame.setVisible(true);
				} catch (Exception e) {
					e.printStackTrace();
				}
			}
		});
	}

	/**
	 * Create the frame.
	 */
	public AsteroidFrame() {
		setResizable(false);
		setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
		setBounds(100, 100, 700, 500);
		contentPane = new JPanel();
		contentPane.setBorder(new EmptyBorder(5, 5, 5, 5));
		contentPane.setLayout(new BorderLayout(0, 0));
		setContentPane(contentPane);
		
		AsteroidView asteroidView = new AsteroidView();
		contentPane.add(asteroidView, BorderLayout.CENTER);
		
        keys = new KeyBoard();
        addKeyListener(keys);
        asteroidView.setKeyBoard(keys);
	}

}
